#include "pcb.h"

//----------------------------------------------------------------------
// PCB::PCB
// 	Initialize pcb
//----------------------------------------------------------------------

PCB::PCB (Thread *input){
	processThread = input;
}

//----------------------------------------------------------------------
// PCB::PCB
// 	De-allocate pcb
//----------------------------------------------------------------------

PCB::~PCB(){
}

//----------------------------------------------------------------------
// PCB::getID
// 	Return PID
//----------------------------------------------------------------------

int PCB::getID(){
	return processID;
}

//----------------------------------------------------------------------
// PCB::getParent
// 	Return parent process
//----------------------------------------------------------------------

PCB* PCB::getParent(){
	return parent_process;
}

//----------------------------------------------------------------------
// PCB::setID
// 	Stores the PID in the pcb
//  "pid" is the process ID
//----------------------------------------------------------------------

void PCB::set(int pid, PCB *parent){
	processID = pid;
	parent_process = parent;
}
